let operator = "/";
let firstNum = 1;
let secondNum = 2;

switch (operator) {
    case "+":
        console.log(firstNum + secondNum);
        break;
    case "-":
        console.log(firstNum - secondNum);
        break;
    case "*":
        console.log(firstNum * secondNum);
        break;
    case "/":
        console.log(firstNum / secondNum);
        break;
    
}